// Function to render all members
function renderMembers(filterText = '') {
    const leadersGrid = document.getElementById('leadersGrid');
    const membersGrid = document.getElementById('membersGrid');
    const noResults = document.getElementById('noResults');
    const paginationContainer = document.getElementById('pagination');

    if (!leadersGrid || !membersGrid) return; // Guard clause if not on members page

    // Filter members based on search
    const filteredMembers = membersData.filter(member =>
        member.name.toLowerCase().includes(filterText.toLowerCase()) ||
        (member.role && member.role.toLowerCase().includes(filterText.toLowerCase()))
    );

    // Update visibility of NO RESULTS section
    if (filteredMembers.length === 0) {
        leadersGrid.style.display = 'none';
        membersGrid.style.display = 'none';
        document.querySelectorAll('.role-section-header').forEach(el => el.style.display = 'none');
        if (noResults) noResults.style.display = 'flex';
        // Hide pagination if no results
        if (paginationContainer) paginationContainer.innerHTML = '';
        return;
    }

    // Reset visibility for grids
    if (noResults) noResults.style.display = 'none';

    // Separate Leaders and Members
    const leaders = filteredMembers.filter(member => member.role === "LEADER" || member.role === "TOP BOXING");
    const members = filteredMembers.filter(member => member.role !== "LEADER" && member.role !== "TOP BOXING");

    // ===========================
    // Render Leaders (Only on Page 1)
    // ===========================
    // Use optional chaining or check existence
    const leadersHeader = leadersGrid.previousElementSibling;

    if (currentPage === 1 && leaders.length > 0) {
        leadersGrid.style.display = 'grid';
        if (leadersHeader) leadersHeader.style.display = 'flex';
        leadersGrid.innerHTML = leaders.map((member, index) => createMemberCard(member, index)).join('');
    } else {
        leadersGrid.style.display = 'none';
        if (leadersHeader) leadersHeader.style.display = 'none';
    }

    // ===========================
    // Render Members (Paginated)
    // ===========================
    const membersHeader = membersGrid.previousElementSibling;

    // Calculate Pagination
    const totalPages = Math.ceil(members.length / itemsPerPage);

    // Ensure currentPage is valid
    if (currentPage > totalPages && totalPages > 0) currentPage = totalPages;
    if (currentPage < 1) currentPage = 1;

    // Calculate Slice
    const start = (currentPage - 1) * itemsPerPage;
    const end = start + itemsPerPage;
    const paginatedMembers = members.slice(start, end);

    if (paginatedMembers.length > 0) {
        membersGrid.style.display = 'grid';
        if (membersHeader) membersHeader.style.display = 'flex';
        // Add animation delay based on index for staggering effect
        membersGrid.innerHTML = paginatedMembers.map((member, index) => createMemberCard(member, index)).join('');
    } else {
        membersGrid.style.display = 'none';
        if (membersHeader) membersHeader.style.display = 'none';
    }

    // Render Pagination Controls
    renderPagination(totalPages);
}

// Function to render pagination buttons
function renderPagination(totalPages) {
    const paginationContainer = document.getElementById('pagination');
    if (!paginationContainer) return;

    if (totalPages <= 1) {
        paginationContainer.innerHTML = '';
        return;
    }

    let buttons = '';

    // Previous Button
    buttons += `<button class="page-btn" onclick="changePage(${currentPage - 1})" ${currentPage === 1 ? 'disabled' : ''}>
        <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><polyline points="15 18 9 12 15 6"></polyline></svg>
    </button>`;

    // Page Numbers logic
    for (let i = 1; i <= totalPages; i++) {
        // Show 1, Last, Current, Current-1, Current+1
        if (i === 1 || i === totalPages || (i >= currentPage - 1 && i <= currentPage + 1)) {
            buttons += `<button class="page-btn ${i === currentPage ? 'active' : ''}" onclick="changePage(${i})">${i}</button>`;
        } else if (i === currentPage - 2 || i === currentPage + 2) {
            buttons += `<span style="color: var(--color-purple-lighter); padding-top: 10px; font-family: var(--font-display);">...</span>`;
        }
    }

    // Next Button
    buttons += `<button class="page-btn" onclick="changePage(${currentPage + 1})" ${currentPage === totalPages ? 'disabled' : ''}>
        <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><polyline points="9 18 15 12 9 6"></polyline></svg>
    </button>`;

    paginationContainer.innerHTML = buttons;
}

// Function to handle page changes
function changePage(page) {
    currentPage = page;
    const searchInput = document.getElementById('searchInput');
    const filterText = searchInput ? searchInput.value : '';

    renderMembers(filterText);

    // Scroll to top of Members section
    const membersSection = document.getElementById('membersGrid').previousElementSibling; // Section Header
    if (membersSection) {
        membersSection.scrollIntoView({ behavior: 'smooth', block: 'start' });
    } else {
        window.scrollTo({ top: 0, behavior: 'smooth' });
    }
}
// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
    // Check if we are on the members page
    const leadersGrid = document.getElementById('leadersGrid');
    if (leadersGrid) {
        renderMembers();
    }

    // Search functionality
    const searchInput = document.getElementById('searchInput');
    if (searchInput) {
        searchInput.addEventListener('input', (e) => {
            currentPage = 1; // Reset to page 1 on search
            renderMembers(e.target.value);
        });
    }
});


// ===========================
// Smooth Scroll & Effects
// ===========================
document.addEventListener('DOMContentLoaded', () => {
    // Smooth scrolling for anchor links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            const href = this.getAttribute('href');
            if (href !== '#' && href.length > 1) {
                e.preventDefault();
                const target = document.querySelector(href);
                if (target) {
                    target.scrollIntoView({
                        behavior: 'smooth',
                        block: 'start'
                    });
                }
            }
        });
    });

    // Add fade-in animation to elements on scroll
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    }, observerOptions);

    // Observe member cards (Dynamic elements need to be observed after rendering)
    // Using a MutationObserver to watch for changes in grids
    const observeNewCards = () => {
        document.querySelectorAll('.member-card, .link-card').forEach(card => {
            // Only observe if not already observed/animated
            if (card.style.opacity === '') {
                card.style.opacity = '0';
                card.style.transform = 'translateY(20px)';
                card.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
                observer.observe(card);
            }
        });
    };

    // Observer config
    const config = { childList: true };
    const mutationCallback = (mutationsList, observer) => {
        for (const mutation of mutationsList) {
            if (mutation.type === 'childList') {
                observeNewCards();
            }
        }
    };

    const gridObserver = new MutationObserver(mutationCallback);
    const leadersGrid = document.getElementById('leadersGrid');
    const membersGrid = document.getElementById('membersGrid');

    if (leadersGrid) gridObserver.observe(leadersGrid, config);
    if (membersGrid) gridObserver.observe(membersGrid, config);

    // Initial observation
    observeNewCards();
});
